package Ques_1;

public class Person {
	String name;
	int age;
	//Constructor with arguments
	public Person(String name,int age) {
		this.name=name;
		this.age=age;
	}
	//getter method to get name
	public String getname() {
		return name;
	}
	//getter method to get age
	public int getage() {
		return age;
	}	
}
